bio["name"] = "Jean";
bio["age"] = 14;

console.log(bio["name"]);
console.log(bio["age"]);

//dot notation   -> myobject.property = somevalue

bio.city = "Santiago";
bio.university = "UTESA";

console.log(bio.city);
console.log(bio.university);


